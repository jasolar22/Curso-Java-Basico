public class switchCase {

    public static void main(String[] args) {

        int option=1;

        switch (option)
        {
            case 0:
            {
                System.out.println("Este es el caso 0");
                break;
            }
            case 1:
            {
                System.out.println("Este es el caso 1");
                break;
            }
            case 2:
            {
                System.out.println("Este es el caso 2");
                break;
            }

        }
    }
}
